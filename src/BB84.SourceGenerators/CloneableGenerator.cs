using System.Collections.Immutable;

using BB84.SourceGenerators.Attributes;
using BB84.SourceGenerators.Extensions;
using BB84.SourceGenerators.Helpers;
using BB84.SourceGenerators.Models;

using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;

namespace BB84.SourceGenerators;

/// <summary>
/// Responsible for generating clone methods for classes and structs marked
/// with the <see cref="GenerateCloneableAttribute"/> attribute. The generated code includes
/// a <c>Clone()</c> method for shallow copying and a <c>DeepClone()</c> method that
/// recursively deep clones reference-type properties also marked with
/// <see cref="GenerateCloneableAttribute"/> and collection properties, implementing <c>ICloneable</c>.
/// </summary>
[Generator(LanguageNames.CSharp)]
public sealed class CloneableGenerator : IIncrementalGenerator
{
	private static readonly string GeneratorAttributeName = typeof(GenerateCloneableAttribute).FullName;

	/// <inheritdoc/>
	public void Initialize(IncrementalGeneratorInitializationContext context)
	{
		// Register for classes
		GeneratorHelpers.RegisterClassGenerator(context, GeneratorAttributeName, ExecuteClass);

		// Register for structs
		IncrementalValuesProvider<(StructDeclarationSyntax StructSyntax, SemanticModel SemanticModel)?> structProvider =
			context.SyntaxProvider
				.ForAttributeWithMetadataName(
					fullyQualifiedMetadataName: GeneratorAttributeName,
					predicate: static (node, _) => node is StructDeclarationSyntax,
					transform: static (ctx, _) => ctx.TargetNode is not StructDeclarationSyntax structSyntax
						? null
						: ((StructDeclarationSyntax StructSyntax, SemanticModel SemanticModel)?)(structSyntax, ctx.SemanticModel))
				.Where(static result => result is not null);

		context.RegisterSourceOutput(structProvider, ExecuteStruct);
	}

	private void ExecuteClass(SourceProductionContext context, (ClassDeclarationSyntax ClassSyntax, SemanticModel SemanticModel)? input)
	{
		if (!GeneratorHelpers.TryCreateContext(input, out GeneratorContext ctx))
			return;

		HashSet<string> excludedProperties = GeneratorHelpers.GetExcludedProperties(ctx.ClassDeclaration, ctx.SemanticModel, nameof(GenerateCloneableAttribute));
		ImmutableArray<PropertyDescriptor> properties = GeneratorHelpers.GetPropertyDescriptors(ctx.ClassSymbol, excludedProperties, requireSetter: true, cloneableAttributeName: GeneratorAttributeName);

		SourceBuilder sb = new();

		sb.AppendAutoGeneratedWarning(GeneratorNames.CloneableGeneratorFullName);
		sb.AppendNullableEnable();
		sb.AppendUsing(NamespaceNames.System);
		AppendCollectionUsings(sb, properties);
		sb.AppendLine();
		sb.OpenNamespace(ctx.NamespaceName);
		sb.OpenOuterClasses(ctx.OuterClasses);

		AppendPartialClass(sb, ctx.ClassName, ctx.Accessibility, properties);

		sb.CloseOuterClasses(ctx.OuterClasses);
		sb.CloseNamespace();

		string hintName = ctx.OuterClasses.Count > 0
			? $"{string.Join(".", ctx.OuterClasses.Select(o => o.Name))}.{ctx.ClassName}.Cloneable.g.cs"
			: $"{ctx.ClassName}.Cloneable.g.cs";

		context.AddSource(hintName, sb.ToString());
	}

	private void ExecuteStruct(SourceProductionContext context, (StructDeclarationSyntax StructSyntax, SemanticModel SemanticModel)? input)
	{
		if (input is null)
			return;

		StructDeclarationSyntax structDeclaration = input.Value.StructSyntax;
		SemanticModel semanticModel = input.Value.SemanticModel;

		INamedTypeSymbol? structSymbol = semanticModel.GetDeclaredSymbol(structDeclaration);
		if (structSymbol is null)
			return;

		string structName = structSymbol.Name;
		string namespaceName = structDeclaration.GetNamespace();
		string accessibility = GeneratorHelpers.GetAccessibility(structDeclaration);

		HashSet<string> excludedProperties = GeneratorHelpers.GetExcludedProperties(structDeclaration, semanticModel, nameof(GenerateCloneableAttribute));
		ImmutableArray<PropertyDescriptor> properties = GeneratorHelpers.GetPropertyDescriptors(structSymbol, excludedProperties, requireSetter: true, cloneableAttributeName: GeneratorAttributeName);

		SourceBuilder sb = new();

		sb.AppendAutoGeneratedWarning(GeneratorNames.CloneableGeneratorFullName);
		sb.AppendNullableEnable();
		sb.AppendUsing(NamespaceNames.System);
		AppendCollectionUsings(sb, properties);
		sb.AppendLine();
		sb.OpenNamespace(namespaceName);

		AppendPartialStruct(sb, structName, accessibility, properties);

		sb.CloseNamespace();

		context.AddSource($"{structName}.Cloneable.g.cs", sb.ToString());
	}

	private static void AppendCollectionUsings(SourceBuilder sb, ImmutableArray<PropertyDescriptor> properties)
	{
		bool needsGeneric = false;
		bool needsImmutable = false;
		bool needsLinq = false;

		foreach (PropertyDescriptor property in properties)
		{
			if (property.CollectionKind is CollectionKind.List or CollectionKind.Dictionary or CollectionKind.ReadOnlyCollection)
				needsGeneric = true;
			if (property.CollectionKind == CollectionKind.ImmutableArray)
				needsImmutable = true;
			if (property.CollectionKind != CollectionKind.None && (property.IsElementCloneable || property.IsDictionaryValueCloneable))
				needsLinq = true;
		}

		if (needsGeneric)
			sb.AppendUsing(NamespaceNames.SystemCollectionsGeneric);
		if (needsImmutable)
			sb.AppendUsing(NamespaceNames.SystemCollectionsImmutable);
		if (needsLinq)
			sb.AppendUsing(NamespaceNames.SystemLinq);
	}

	private static void AppendPartialClass(SourceBuilder sb, string className, string accessibility, ImmutableArray<PropertyDescriptor> properties)
	{
		sb.OpenClass(accessibility, className, "ICloneable");

		AppendCloneMethod(sb, className, properties, isStruct: false);
		sb.AppendLine();
		AppendDeepCloneMethod(sb, className, properties, isStruct: false);
		sb.AppendLine();
		AppendICloneableClone(sb);

		sb.CloseClass();
	}

	private static void AppendPartialStruct(SourceBuilder sb, string structName, string accessibility, ImmutableArray<PropertyDescriptor> properties)
	{
		sb.AppendLine($"{accessibility} partial struct {structName} : ICloneable");
		sb.OpenBrace();

		AppendCloneMethod(sb, structName, properties, isStruct: true);
		sb.AppendLine();
		AppendDeepCloneMethod(sb, structName, properties, isStruct: true);
		sb.AppendLine();
		AppendICloneableClone(sb);

		sb.CloseBrace();
	}

	private static void AppendCloneMethod(SourceBuilder sb, string typeName, ImmutableArray<PropertyDescriptor> properties, bool isStruct)
	{
		sb.AppendLine("/// <summary>");
		sb.AppendLine($"/// Creates a shallow copy of this <see cref=\"{typeName}\"/> instance.");
		sb.AppendLine("/// </summary>");
		sb.AppendLine($"/// <returns>A new <see cref=\"{typeName}\"/> instance with the same property values.</returns>");
		sb.AppendLine($"public {typeName} Clone()");
		sb.OpenBrace();

		if (isStruct)
		{
			sb.AppendLine($"{typeName} clone = this;");
		}
		else
		{
			sb.AppendLine($"{typeName} clone = new {typeName}();");
			foreach (PropertyDescriptor property in properties)
				sb.AppendLine($"clone.{property.Name} = this.{property.Name};");
		}

		sb.AppendLine("return clone;");
		sb.CloseBrace();
	}

	private static void AppendDeepCloneMethod(SourceBuilder sb, string typeName, ImmutableArray<PropertyDescriptor> properties, bool isStruct)
	{
		sb.AppendLine("/// <summary>");
		sb.AppendLine($"/// Creates a deep copy of this <see cref=\"{typeName}\"/> instance.");
		sb.AppendLine("/// Reference-type properties marked with <c>[GenerateCloneable]</c> are recursively deep cloned.");
		sb.AppendLine("/// Collection properties are deep copied with their elements.");
		sb.AppendLine("/// </summary>");
		sb.AppendLine($"/// <returns>A new <see cref=\"{typeName}\"/> instance with deeply cloned property values.</returns>");
		sb.AppendLine($"public {typeName} DeepClone()");
		sb.OpenBrace();

		if (isStruct)
			sb.AppendLine($"{typeName} clone = this;");
		else
			sb.AppendLine($"{typeName} clone = new {typeName}();");

		foreach (PropertyDescriptor property in properties)
		{
			AppendDeepCloneProperty(sb, property);
		}

		sb.AppendLine("return clone;");
		sb.CloseBrace();
	}

	private static void AppendDeepCloneProperty(SourceBuilder sb, PropertyDescriptor property)
	{
		string src = $"this.{property.Name}";

		switch (property.CollectionKind)
		{
			case CollectionKind.List:
				if (property.IsElementCloneable)
				{
					if (property.IsNullable)
						sb.AppendLine($"clone.{property.Name} = {src}?.Select(x => x.DeepClone()).ToList();");
					else
						sb.AppendLine($"clone.{property.Name} = {src}.Select(x => x.DeepClone()).ToList();");
				}
				else
				{
					if (property.IsNullable)
						sb.AppendLine($"clone.{property.Name} = {src} is not null ? new List<{property.ElementTypeName}>({src}) : null;");
					else
						sb.AppendLine($"clone.{property.Name} = new List<{property.ElementTypeName}>({src});");
				}
				break;

			case CollectionKind.Dictionary:
				if (property.IsDictionaryValueCloneable)
				{
					if (property.IsNullable)
						sb.AppendLine($"clone.{property.Name} = {src}?.ToDictionary(kvp => kvp.Key, kvp => kvp.Value.DeepClone());");
					else
						sb.AppendLine($"clone.{property.Name} = {src}.ToDictionary(kvp => kvp.Key, kvp => kvp.Value.DeepClone());");
				}
				else
				{
					if (property.IsNullable)
						sb.AppendLine($"clone.{property.Name} = {src} is not null ? new Dictionary<{property.ElementTypeName}, {property.DictionaryValueTypeName}>({src}) : null;");
					else
						sb.AppendLine($"clone.{property.Name} = new Dictionary<{property.ElementTypeName}, {property.DictionaryValueTypeName}>({src});");
				}
				break;

			case CollectionKind.Array:
				if (property.IsElementCloneable)
				{
					if (property.IsNullable)
						sb.AppendLine($"clone.{property.Name} = {src}?.Select(x => x.DeepClone()).ToArray();");
					else
						sb.AppendLine($"clone.{property.Name} = {src}.Select(x => x.DeepClone()).ToArray();");
				}
				else
				{
					if (property.IsNullable)
						sb.AppendLine($"clone.{property.Name} = {src} is not null ? ({property.TypeName}){src}.Clone() : null;");
					else
						sb.AppendLine($"clone.{property.Name} = ({property.TypeName}){src}.Clone();");
				}
				break;

			case CollectionKind.ImmutableArray:
				if (property.IsElementCloneable)
				{
					sb.AppendLine($"clone.{property.Name} = {src}.Select(x => x.DeepClone()).ToImmutableArray();");
				}
				else
				{
					// ImmutableArray is a value type; shallow copy is fine for non-cloneable elements
					sb.AppendLine($"clone.{property.Name} = {src};");
				}
				break;

			case CollectionKind.ReadOnlyCollection:
				if (property.IsElementCloneable)
				{
					if (property.IsNullable)
						sb.AppendLine($"clone.{property.Name} = {src}?.Select(x => x.DeepClone()).ToList().AsReadOnly();");
					else
						sb.AppendLine($"clone.{property.Name} = {src}.Select(x => x.DeepClone()).ToList().AsReadOnly();");
				}
				else
				{
					if (property.IsNullable)
						sb.AppendLine($"clone.{property.Name} = {src} is not null ? new List<{property.ElementTypeName}>({src}).AsReadOnly() : null;");
					else
						sb.AppendLine($"clone.{property.Name} = new List<{property.ElementTypeName}>({src}).AsReadOnly();");
				}
				break;

			default:
				if (property.IsCloneable)
					sb.AppendLine($"clone.{property.Name} = {src}?.DeepClone();");
				else
					sb.AppendLine($"clone.{property.Name} = {src};");
				break;
		}
	}

	private static void AppendICloneableClone(SourceBuilder sb)
	{
		sb.AppendLine("/// <inheritdoc/>");
		sb.AppendLine("object ICloneable.Clone()");
		sb.Indent();
		sb.AppendLine("=> DeepClone();");
		sb.Outdent();
	}
}
