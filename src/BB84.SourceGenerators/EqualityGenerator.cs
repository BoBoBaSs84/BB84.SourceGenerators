using System.Collections.Immutable;
using System.Text;

using BB84.SourceGenerators.Attributes;
using BB84.SourceGenerators.Extensions;
using BB84.SourceGenerators.Helpers;
using BB84.SourceGenerators.Models;

using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;

namespace BB84.SourceGenerators;

/// <summary>
/// Responsible for generating equality members for classes marked
/// with the <see cref="GenerateEqualityAttribute"/> attribute. The generated code includes
/// <c>Equals(object)</c>, <c>Equals(T)</c>, <c>GetHashCode()</c>,
/// <c>operator ==</c>, and <c>operator !=</c>, implementing <c>IEquatable&lt;T&gt;</c>.
/// </summary>
[Generator(LanguageNames.CSharp)]
public sealed class EqualityGenerator : IIncrementalGenerator
{
	private static readonly string GeneratorName = typeof(EqualityGenerator).FullName;
	private static readonly string GeneratorAttributeName = typeof(GenerateEqualityAttribute).FullName;

	/// <inheritdoc/>
	public void Initialize(IncrementalGeneratorInitializationContext context)
	{
		IncrementalValuesProvider<(ClassDeclarationSyntax ClassSyntax, SemanticModel SemanticModel)?> provider =
			context.SyntaxProvider
				.ForAttributeWithMetadataName(
					fullyQualifiedMetadataName: GeneratorAttributeName,
					predicate: static (node, _) => node is ClassDeclarationSyntax,
					transform: static (ctx, _) => GeneratorHelpers.TransformClassSyntax(ctx))
				.Where(static result => result is not null);

		context.RegisterSourceOutput(provider, Execute);
	}

	private void Execute(SourceProductionContext context, (ClassDeclarationSyntax ClassSyntax, SemanticModel SemanticModel)? input)
	{
		if (!GeneratorHelpers.TryCreateContext(input, out GeneratorContext ctx))
			return;

		HashSet<string> excludedProperties = GeneratorHelpers.GetExcludedProperties(ctx.ClassDeclaration, ctx.SemanticModel, "GenerateEquality", "GenerateEqualityAttribute");
		ImmutableArray<PropertyDescriptor> properties = GeneratorHelpers.GetPropertyDescriptors(ctx.ClassSymbol, excludedProperties);

		StringBuilder sb = new();

		sb.AppendAutoGeneratedWarning(GeneratorName);
		sb.AppendLine("#nullable enable");
		sb.AppendLine("using System;");
		sb.AppendLine();
		sb.AppendLine($"namespace {ctx.NamespaceName}");
		sb.AppendLine("{");

		int baseIndent = 1;
		foreach ((string outerAccessibility, string outerName) in ctx.OuterClasses)
		{
			string indent = new(' ', baseIndent * 2);
			sb.AppendLine($"{indent}{outerAccessibility} partial class {outerName}");
			sb.AppendLine($"{indent}{{");
			baseIndent++;
		}

		AppendPartialClass(sb, ctx.ClassName, ctx.Accessibility, properties, baseIndent);

		for (int i = ctx.OuterClasses.Count - 1; i >= 0; i--)
		{
			baseIndent--;
			string indent = new(' ', baseIndent * 2);
			sb.AppendLine($"{indent}}}");
		}

		sb.AppendLine("}");

		string hintName = ctx.OuterClasses.Count > 0
			? $"{string.Join(".", ctx.OuterClasses.Select(o => o.Name))}.{ctx.ClassName}.Equality.g.cs"
			: $"{ctx.ClassName}.Equality.g.cs";

		context.AddSource(hintName, sb.ToString());
	}

	private static void AppendPartialClass(StringBuilder sb, string className, string accessibility, ImmutableArray<PropertyDescriptor> properties, int indentLevel = 1)
	{
		string indent = new(' ', indentLevel * 2);

		sb.AppendLine($"{indent}{accessibility} partial class {className} : IEquatable<{className}>");
		sb.AppendLine($"{indent}{{");

		AppendEqualsObject(sb, className, indentLevel + 1);
		sb.AppendLine();
		AppendEqualsT(sb, className, properties, indentLevel + 1);
		sb.AppendLine();
		AppendGetHashCode(sb, properties, indentLevel + 1);
		sb.AppendLine();
		AppendOperatorEquals(sb, className, indentLevel + 1);
		sb.AppendLine();
		AppendOperatorNotEquals(sb, className, indentLevel + 1);

		sb.AppendLine($"{indent}}}");
	}

	private static void AppendEqualsObject(StringBuilder sb, string className, int indentLevel)
	{
		string indent = new(' ', indentLevel * 2);
		string bodyIndent = new(' ', (indentLevel + 1) * 2);

		sb.AppendLine($"{indent}/// <inheritdoc/>");
		sb.AppendLine($"{indent}public override bool Equals(object? obj)");
		sb.AppendLine($"{bodyIndent}=> Equals(obj as {className});");
	}

	private static void AppendEqualsT(StringBuilder sb, string className, ImmutableArray<PropertyDescriptor> properties, int indentLevel)
	{
		string indent = new(' ', indentLevel * 2);
		string bodyIndent = new(' ', (indentLevel + 1) * 2);
		string deepIndent = new(' ', (indentLevel + 2) * 2);

		sb.AppendLine($"{indent}/// <inheritdoc/>");
		sb.AppendLine($"{indent}public bool Equals({className}? other)");
		sb.AppendLine($"{indent}{{");
		sb.AppendLine($"{bodyIndent}if (other is null)");
		sb.AppendLine($"{deepIndent}return false;");
		sb.AppendLine();
		sb.AppendLine($"{bodyIndent}if (ReferenceEquals(this, other))");
		sb.AppendLine($"{deepIndent}return true;");

		if (properties.Length > 0)
		{
			sb.AppendLine();
			sb.Append($"{bodyIndent}return ");

			for (int i = 0; i < properties.Length; i++)
			{
				if (i > 0)
				{
					sb.AppendLine();
					sb.Append($"{deepIndent}&& ");
				}

				sb.Append($"Equals({properties[i].Name}, other.{properties[i].Name})");
			}

			sb.AppendLine(";");
		}
		else
		{
			sb.AppendLine();
			sb.AppendLine($"{bodyIndent}return true;");
		}

		sb.AppendLine($"{indent}}}");
	}

	private static void AppendGetHashCode(StringBuilder sb, ImmutableArray<PropertyDescriptor> properties, int indentLevel)
	{
		string indent = new(' ', indentLevel * 2);
		string bodyIndent = new(' ', (indentLevel + 1) * 2);
		string deepIndent = new(' ', (indentLevel + 2) * 2);

		sb.AppendLine($"{indent}/// <inheritdoc/>");
		sb.AppendLine($"{indent}public override int GetHashCode()");
		sb.AppendLine($"{indent}{{");

		if (properties.Length == 0)
		{
			sb.AppendLine($"{bodyIndent}return base.GetHashCode();");
		}
		else
		{
			sb.AppendLine($"{bodyIndent}unchecked");
			sb.AppendLine($"{bodyIndent}{{");
			sb.AppendLine($"{deepIndent}int hash = 17;");

			foreach (PropertyDescriptor property in properties)
			{
				if (property.IsValueType)
					sb.AppendLine($"{deepIndent}hash = hash * 31 + {property.Name}.GetHashCode();");
				else
					sb.AppendLine($"{deepIndent}hash = hash * 31 + ({property.Name}?.GetHashCode() ?? 0);");
			}

			sb.AppendLine($"{deepIndent}return hash;");
			sb.AppendLine($"{bodyIndent}}}");
		}

		sb.AppendLine($"{indent}}}");
	}

	private static void AppendOperatorEquals(StringBuilder sb, string className, int indentLevel)
	{
		string indent = new(' ', indentLevel * 2);
		string bodyIndent = new(' ', (indentLevel + 1) * 2);

		sb.AppendLine($"{indent}/// <summary>");
		sb.AppendLine($"{indent}/// Determines whether two <see cref=\"{className}\"/> instances are equal.");
		sb.AppendLine($"{indent}/// </summary>");
		sb.AppendLine($"{indent}public static bool operator ==({className}? left, {className}? right)");
		sb.AppendLine($"{bodyIndent}=> Equals(left, right);");
	}

	private static void AppendOperatorNotEquals(StringBuilder sb, string className, int indentLevel)
	{
		string indent = new(' ', indentLevel * 2);
		string bodyIndent = new(' ', (indentLevel + 1) * 2);

		sb.AppendLine($"{indent}/// <summary>");
		sb.AppendLine($"{indent}/// Determines whether two <see cref=\"{className}\"/> instances are not equal.");
		sb.AppendLine($"{indent}/// </summary>");
		sb.AppendLine($"{indent}public static bool operator !=({className}? left, {className}? right)");
		sb.AppendLine($"{bodyIndent}=> !Equals(left, right);");
	}
}
